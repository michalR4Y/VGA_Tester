// Funkcja rysująca piksel (x: 0-639, y: 0-479)
void VGA_DrawPixel(int16_t x, int16_t y, uint8_t color)
{
  if(x < 0 || x >= VGA_WIDTH || y < 0 || y >= VGA_HEIGHT)
  {
    return;
  }
  if(color)
  {
    frame_buffer[y * LINE_BYTES + (x / 8)] |= (0x80 >> (x % 8));
  }
  else
  {
    frame_buffer[y * LINE_BYTES + (x / 8)] &= ~(0x80 >> (x % 8));
  }
}

// Funkcja czyszcząca ekran
void VGA_Clear(uint8_t color)
{
  memset(frame_buffer, 0x00, sizeof(frame_buffer));
}

void VGA_DrawChar(int16_t x, int16_t y, char c, uint8_t color)
{
  if(c < 32 || c > 126)
    c = ' ';
  const uint8_t *glyph = font8x8[c - 0x20];
  for(int i = 0; i < 8; i++)
  {
    for(int j = 0; j < 8; j++)
    {
      if(glyph[i] & (0x80 >> j))
      {
        VGA_DrawPixel(x + j, y + i, color);
      }
    }
  }
}

void VGA_PrintString(int16_t x, int16_t y, char *str, uint8_t color)
{
  while(*str)
  {
    VGA_DrawChar(x, y, *str++, color);
    x += 8;
    if(x + 8 >= VGA_WIDTH)
    {
      x = 0;
      y += 8;
    }
  }
}

/**
 * @brief Przerwanie Timera 1 - wywoływane co 31.7us (co linię H-Sync)
 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if(htim->Instance == TIM1)
  {
    // VGA Active Video: Linie od ok. 35 do 515 (uwzględniając Back Porch)
    if(v_line_counter >= 35 && v_line_counter < 480 + 35)
    {
      uint16_t current_display_line = v_line_counter - 35;

      // Start transferu DMA dla jednej linii (80 bajtów)
      // Używamy niskopoziomowego rejestru dla szybkości
      HAL_SPI_Transmit_DMA(&hspi2, &frame_buffer[current_display_line * LINE_BYTES], LINE_BYTES);
    }

    v_line_counter++;
    if(v_line_counter >= 525)
    {
      v_line_counter = 0;
    }
  }
}

void VGA_Init()
{
  // Start sygnałów synchronizacji
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1); // H-Sync
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2); // V-Sync

  // Włączenie przerwania TIM1, aby wiedzieć, kiedy zacząć wysyłać linię danych
  HAL_TIM_Base_Start_IT(&htim1);
}
